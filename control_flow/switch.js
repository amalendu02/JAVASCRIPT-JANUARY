const month = 7

switch (month) {
    case 1:
        console.log("January");
        break;

    case 2:
        console.log("feb");
        break;    

    case 3:
        console.log("march");
        break;
    case 4:
        console.log("April");
        break;
    default:
        console.log("default case presented")
        break;
}